#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	const char	*s1;
	char		*s2;

	s2 = dst;
	s1 = src;
	while (n > 0)
	{
		*s2 = *s1;
		s1++;
		s2++;
		n--;
	}
	return (dst);
}
